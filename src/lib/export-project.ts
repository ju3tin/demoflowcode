import JSZip from "jszip";
import { buildDeployScript, NETWORK_CONFIG } from "@/lib/deploy-script";

function extractConstructorParams(code: string): string {
  const match = code.match(/constructor\s*\(([^)]*)\)/);
  return match ? match[1].trim() : "";
}

function extractSolidityVersion(code: string): string {
  const match = code.match(/pragma solidity\s+([^;]+);/);
  if (!match) return "0.8.24";
  const versionMatch = match[1].match(/(\d+\.\d+\.\d+)/);
  return versionMatch ? versionMatch[1] : "0.8.24";
}

function toPackageName(contractName: string) {
  const kebab = contractName
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .toLowerCase()
    .replace(/[^a-z0-9-]/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
  return kebab || "avalanche-contract";
}

function hardhatConfig(version: string) {
  return `import { HardhatUserConfig } from "hardhat/config";
import "@nomicfoundation/hardhat-toolbox";
import * as dotenv from "dotenv";
dotenv.config();

const config: HardhatUserConfig = {
  solidity: "${version}",
  networks: {
    fuji: {
      url: "${NETWORK_CONFIG.fuji.rpcUrl}",
      chainId: ${NETWORK_CONFIG.fuji.chainId},
      accounts: process.env.PRIVATE_KEY ? [process.env.PRIVATE_KEY] : [],
    },
    avalanche: {
      url: "${NETWORK_CONFIG["avalanche-mainnet"].rpcUrl}",
      chainId: ${NETWORK_CONFIG["avalanche-mainnet"].chainId},
      accounts: process.env.PRIVATE_KEY ? [process.env.PRIVATE_KEY] : [],
    },
  },
};

export default config;
`;
}

function packageJson(name: string) {
  return JSON.stringify(
    {
      name,
      version: "1.0.0",
      private: true,
      scripts: {
        compile: "hardhat compile",
        "deploy:fuji": "hardhat run scripts/deploy.ts --network fuji",
        "deploy:mainnet": "hardhat run scripts/deploy.ts --network avalanche",
      },
      devDependencies: {
        "@nomicfoundation/hardhat-toolbox": "^5.0.0",
        hardhat: "^2.22.0",
        dotenv: "^16.4.0",
        typescript: "^5.5.0",
      },
      dependencies: {
        "@openzeppelin/contracts": "^5.0.0",
      },
    },
    null,
    2
  );
}

