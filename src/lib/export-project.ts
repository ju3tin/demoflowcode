function gitignore() {
  return `node_modules
.env
artifacts
cache
typechain-types
`;
}

export interface ExportProjectInput {
  code: string;
  contractName: string;
  constructorArgs?: string;
}

export async function exportProjectZip({
  code,
  contractName,
  constructorArgs,
}: ExportProjectInput): Promise<Blob> {
  const name = contractName?.trim() || "MyContract";
  const version = extractSolidityVersion(code);
  const { deployScript } = buildDeployScript({
    contractName: name,
    network: "fuji",
    constructorArgs,
  });

  const zip = new JSZip();
  zip.file(`contracts/${name}.sol`, code);
  zip.file("scripts/deploy.ts", deployScript);
  zip.file("hardhat.config.ts", hardhatConfig(version));
  zip.file("package.json", packageJson(toPackageName(name)));
  zip.file(".env.example", envExample());
  zip.file(".gitignore", gitignore());

  const constructorParams = extractConstructorParams(code);
  const constructorNote =
    constructorParams && !constructorArgs?.trim()
      ? `\n**This contract's constructor takes arguments** (\`${constructorParams}\`) — open \`scripts/deploy.ts\` and fill them into the \`.deploy(...)\` call before running it, or the deploy will fail.\n`
      : "";
  zip.file("README.md", readme(name, constructorNote));

  return zip.generateAsync({ type: "blob" });
}

export function downloadBlob(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  a.remove();
  URL.revokeObjectURL(url);
}
